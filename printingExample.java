package printingExample;

public class printingExample {
	public static void main(String...S) {
		double a=26.58412887;
		System.out.println(a);
		System.out.print(a);
		System.out.print(a+"%n");
		System.out.print(a+"\n");
		System.out.printf("%f\n",a);
		System.out.printf("%f%n",a);
		System.out.printf("%.2f%n",a);
		
	}

}
